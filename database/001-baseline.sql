# Oracle Schema for SUBSCRIPTION_MANAGER

```sql
-- Create user
CREATE USER SUBSCRIPTION_MANAGER IDENTIFIED BY th1nksk1nk;

-- Grant basic permissions
GRANT CONNECT, RESOURCE TO SUBSCRIPTION_MANAGER;
GRANT CREATE SESSION TO SUBSCRIPTION_MANAGER;
GRANT CREATE TABLE TO SUBSCRIPTION_MANAGER;
GRANT CREATE SEQUENCE TO SUBSCRIPTION_MANAGER;

-- =========================
-- CLIENT TABLE
-- =========================

-- Sequence for CLIENT table
CREATE SEQUENCE SUBSCRIPTION_MANAGER.SEQ_CLIENT_ID
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- CLIENT table
CREATE TABLE SUBSCRIPTION_MANAGER.CLIENT (
    CLIENT_ID      NUMBER PRIMARY KEY,
    NAME           VARCHAR2(400),
    LAST_NAME      VARCHAR2(400),
    EMAIL          VARCHAR2(400),
    MSISDN         VARCHAR2(400)
);

-- Trigger for automatic CLIENT_ID generation
CREATE OR REPLACE TRIGGER SUBSCRIPTION_MANAGER.TRG_CLIENT_ID
BEFORE INSERT ON SUBSCRIPTION_MANAGER.CLIENT
FOR EACH ROW
BEGIN
    IF :NEW.CLIENT_ID IS NULL THEN
        SELECT SUBSCRIPTION_MANAGER.SEQ_CLIENT_ID.NEXTVAL
        INTO :NEW.CLIENT_ID
        FROM DUAL;
    END IF;
END;
/

-- =========================
-- SUBSCRIPTIONS TABLE
-- =========================

-- Sequence for SUBSCRIPTIONS table
CREATE SEQUENCE SUBSCRIPTION_MANAGER.SEQ_SUBSCRIPTION_ID
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- SUBSCRIPTIONS table
CREATE TABLE SUBSCRIPTION_MANAGER.SUBSCRIPTIONS (
    ID                  NUMBER PRIMARY KEY,
    CLIENT_ID           NUMBER,
    ENTRY_DATE          DATE,
    MODIFY_DATE         DATE,
    PLATFORM            VARCHAR2(100),
    CONTRACT            VARCHAR2(400),
    STATUS              VARCHAR2(2),
    PO                  VARCHAR2(400),
    ACTIVATE_DATE       DATE,
    DEACTIVATE_DATE     DATE,
    CANCEL_DATE         DATE,
    START_TRIAL_DATE    DATE,
    END_TRIAL_DATE      DATE,
    AMOUNT              NUMBER(15,2),
    TRANSACTION_DATE    DATE,
    FLOW                VARCHAR2(400),
    OBSERVATION         VARCHAR2(4000),
    ERROR_CODE          VARCHAR2(400),
    ERROR_MSG           VARCHAR2(4000),
    PROMOTION           NUMBER,
    CONSTRAINT FK_SUBSCRIPTIONS_CLIENT
        FOREIGN KEY (CLIENT_ID)
        REFERENCES SUBSCRIPTION_MANAGER.CLIENT (CLIENT_ID)
);

-- Trigger for automatic SUBSCRIPTION ID generation
CREATE OR REPLACE TRIGGER SUBSCRIPTION_MANAGER.TRG_SUBSCRIPTION_ID
BEFORE INSERT ON SUBSCRIPTION_MANAGER.SUBSCRIPTIONS
FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT SUBSCRIPTION_MANAGER.SEQ_SUBSCRIPTION_ID.NEXTVAL
        INTO :NEW.ID
        FROM DUAL;
    END IF;
END;
/
```

Notes:

* `NUMBER(15,2)` is used for money values.
* `DATE` is used for all date fields.
* `CLIENT_ID` in `SUBSCRIPTIONS` is a foreign key to `CLIENT`.
* Sequences + triggers automatically generate IDs.



